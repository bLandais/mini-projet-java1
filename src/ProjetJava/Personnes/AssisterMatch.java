package ProjetJava.Personnes;

/**
 * The interface Assister match mettant à disposition les méthodes communes aux spectateurs et aux joueurs pour assister à un match.
 */
public interface AssisterMatch {

    /**
     * Applaudir.
     */
    void applaudir();


    /**
     * Crier.
     */
    void crier();


    /**
     * Huer.
     */
    void huer();


    /**
     * Dormir.
     */
    void dormir();
}
